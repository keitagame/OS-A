[org 0x7c00]        ; BIOSがブートローダを読み込むメモリの開始アドレス

mov ah, 0x0e        ; BIOS割り込み(INT 0x10)の文字出力ファンクション番号
mov bx, message     ; 文字列のアドレスをBXレジスタに代入

print_loop:
    mov al, [bx]    ; BXが指すアドレスの文字をALに読み込む
    cmp al, 0       ; 文字列の終わり（ヌル文字）かチェック
    je hang         ; 終わりならhangへジャンプ
    int 0x10        ; BIOSのビデオサービス割り込みを実行して文字を表示
    inc bx          ; 次の文字のアドレスへ進める
    jmp print_loop  ; ループを繰り返す

hang:
    jmp $           ; 現在の行へジャンプし続け、CPUを無限ループさせる

message:
    db "Hello, Bootloader!", 0  ; 出力する文字列（最後に0を置く）

; -------------------------------------------------------------------------
; ブートセクタのルール：全体で512バイトにし、末尾2バイトを「0xaa55」にする
; -------------------------------------------------------------------------
times 510 - ($ - $$) db 0  ; 現在のプログラムサイズから510バイト目までを0で埋める
dw 0xaa55                  ; ブートシグネチャ（16進数でAA55を書き込む）
